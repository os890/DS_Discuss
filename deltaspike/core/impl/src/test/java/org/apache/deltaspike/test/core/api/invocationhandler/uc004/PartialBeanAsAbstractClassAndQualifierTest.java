package org.apache.deltaspike.test.core.api.invocationhandler.uc004;

import org.apache.deltaspike.test.core.api.invocationhandler.shared.PartialBeanBinding;
import org.apache.deltaspike.test.util.ArchiveUtils;
import org.jboss.arquillian.container.test.api.Deployment;
import org.jboss.arquillian.junit.Arquillian;
import org.jboss.shrinkwrap.api.ShrinkWrap;
import org.jboss.shrinkwrap.api.asset.EmptyAsset;
import org.jboss.shrinkwrap.api.spec.JavaArchive;
import org.jboss.shrinkwrap.api.spec.WebArchive;
import org.junit.Assert;
import org.junit.Test;
import org.junit.runner.RunWith;

import javax.inject.Inject;

@RunWith(Arquillian.class)
public class PartialBeanAsAbstractClassAndQualifierTest
{
    @Inject
    @CustomQualifier
    private PartialBean partialBean;

    @Deployment
    public static WebArchive war()
    {
        String simpleName = PartialBeanAsAbstractClassAndQualifierTest.class.getSimpleName();
        String archiveName = simpleName.substring(0, 1).toLowerCase() + simpleName.substring(1);

        JavaArchive testJar = ShrinkWrap.create(JavaArchive.class, archiveName + ".jar")
                .addPackage(PartialBeanAsAbstractClassAndQualifierTest.class.getPackage())
                .addPackage(PartialBeanBinding.class.getPackage())
                .addAsManifestResource(EmptyAsset.INSTANCE, "beans.xml");

        return ShrinkWrap.create(WebArchive.class, archiveName + ".war")
                .addAsLibraries(ArchiveUtils.getDeltaSpikeCoreArchive())
                .addAsLibraries(testJar)
                .addAsWebInfResource(EmptyAsset.INSTANCE, "beans.xml");
    }

    @Test
    public void testPartialBeanAsAbstractClassAndQualifier() throws Exception
    {
        String result = this.partialBean.getResult();

        Assert.assertEquals("PARTIAL-TEST-FALSE", result);

        result = this.partialBean.getManualResult();

        Assert.assertEquals("manual-test", result);

        //TODO test pre-destroy callback
    }
}
